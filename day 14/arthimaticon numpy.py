# Create arrays
import numpy as np
arr1 = np.array([10, 20, 30])
arr2 = np.array([1, 2, 3])

# Perform arithmetic operations
print("Addition:", arr1 + arr2)  # [11, 22, 33]
print("Subtraction:", arr1 - arr2)  # [9, 18, 27]
print("Multiplication:", arr1 * arr2)  # [10, 40, 90]
print("Division:", arr1 / arr2)  # [10, 10, 10]
